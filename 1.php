    $("#getQuoteBTN").click(function(e) {
		e.preventDefault();
		$("#demoForm-ThankYou").slideUp('slow');
		$("#demoForm-Container").slideDown('slow');
		ga('send', 'event', 'button', 'click','Get Quote');
	});
	$("#viewDemoBTN").click(function(e) {
		e.preventDefault();
		ga('send', 'event', 'button', 'click','View Demo');
		location.href='https://amanziportal.com/demo1/';
	});
	
	$("#quoteForm").submit(function(e) {
		e.preventDefault();
		var formData = $("form#quoteForm").serialize();
		if ( $("#lname").val() != '') {
			location.href='http://www.mischiefmakersmanual.com/tools/annoy/homepage.html';
		} else {
			$.post("sendquote.php",formData,function(data) {
				if (data == '1') {
					$('form#quoteForm').find("input[type=text],input[type=email],input[type=tel]").val("");
					$("#demoForm-Container").slideUp('slow');
					$("#demoForm-ThankYou").slideDown('slow');
					$("#quoteContainer").slideUp('slow');
					$("#unlimitedContainer").css("background-color","#FAFAFA");
					ga('send', 'event', 'form', 'submit','Quote 1');
					fbq('track', 'Lead');
				} else {
					alert(data);
				}
			});
		}
	});
	$("#quoteForm2").submit(function(e) {
		e.preventDefault();
		var formData = $("form#quoteForm2").serialize();
		if ( $("#quotelname").val() != '') {
			location.href='http://www.mischiefmakersmanual.com/tools/annoy/homepage.html';
		} else {
			$.post("sendquote.php",formData,function(data) {
				if (data == '1') {
					$('form#quoteForm2').find("input[type=text],input[type=email],input[type=tel]").val("");
					$("form#quoteForm2").slideUp('slow');
					$("input#getQuoteBTN").slideUp('slow');
					$("#demoForm2-ThankYou").slideDown('slow');
					ga('send', 'event', 'form', 'submit','Quote 2');
					fbq('track', 'Lead');
				} else {
					alert(data);
				}
			});
		}
	});

});
